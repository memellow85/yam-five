<script>
import { mapState } from 'vuex'
import NavigationMixin from '~/mixins/navigation'
import ExitMixin from '~/mixins/exit'
// import { toDateTime, formatDate } from '~/utils'

export default {
  mixins: [NavigationMixin, ExitMixin],
  data() {
    return {
      subMenu: [
        {
          title: this.$t('submenu.menu_0'),
          name: 'info',
          icon: 'shield-account-outline',
        },
        {
          title: this.$t('submenu.menu_1'),
          name: 'game-help',
          icon: 'head-question-outline',
        },
        {
          title: this.$t('submenu.menu_2'),
          name: 'game-stats',
          icon: 'chart-box-outline',
        },
        {
          title: this.$t('submenu.menu_3'),
          name: 'game-games',
          icon: 'view-grid-outline',
        },
        {
          title: this.$t('submenu.menu_4'),
          name: 'game-invite',
          icon: 'account-plus-outline',
        },
        {
          title: this.$t('submenu.menu_5'),
          name: 'game-config',
          icon: 'cog-outline',
        },
      ],
    }
  },
  computed: {
    ...mapState('firebase', {
      userDetailsFirebase: (state) => state.userDetailsFirebase,
      userFirebase: (state) => state.userFirebase,
    }),
    ...mapState('ws', {
      userSocket: (state) => state.userSocket,
      loginUsersSocket: (state) => state.loginUsersSocket,
    }),
    /* ...mapState('game', {
      campaignActive: (state) => state.campaignActive,
      currentCampaign: (state) => state.currentCampaign,
    }), */
    firstLetter() {
      if (this.userDetailsFirebase) {
        if (this.userDetailsFirebase.name.split(' ').length > 1) {
          return (
            this.userDetailsFirebase.name.split(' ')[0].charAt(0) +
            this.userDetailsFirebase.name.split(' ')[1].charAt(0)
          )
        } else {
          return (
            this.userDetailsFirebase.name.split(' ')[0].charAt(0) +
            this.userDetailsFirebase.name.split(' ')[0].charAt(1)
          )
        }
      } else {
        return '-'
      }
    },
    getName() {
      if (this.userDetailsFirebase) {
        return this.userDetailsFirebase.name.split(' ')[0]
      } else {
        return '-'
      }
    },
    /* dateLastMatch() {
      if (this.userDetailsFirebase) {
        return formatDate(
          toDateTime(this.userDetailsFirebase.last_updated.seconds)
        )
      } else {
        return '-'
      }
    }, */
    getUsersLogin() {
      return this.loginUsersSocket.length > 0
        ? this.loginUsersSocket.filter(
            (u) => this.userFirebase && u.uid !== this.userFirebase.uid
          )
        : []
    },
  },
  methods: {
    showSubMenuHandler() {
      this.$store.commit(`game/showSubMenu`, false)
    },
    leaveHandler() {
      this.$store.dispatch('ws/leftRoomSocket')
    },
  },
}
</script>

